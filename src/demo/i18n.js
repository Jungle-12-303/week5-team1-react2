function formatMessage(template, params = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? `{${key}}`));
}

export function createI18n(initialLanguage = "ko") {
  const state = {
    language: Object.prototype.hasOwnProperty.call(MESSAGES, initialLanguage) ? initialLanguage : "ko",
  };

  return {
    getLanguage() {
      return state.language;
    },
    setLanguage(nextLanguage) {
      if (!Object.prototype.hasOwnProperty.call(MESSAGES, nextLanguage)) {
        return state.language;
      }

      state.language = nextLanguage;
      return state.language;
    },
    t(key, params = {}) {
      const currentMessages = MESSAGES[state.language];
      const fallbackMessages = MESSAGES.en;
      const template = currentMessages[key] ?? fallbackMessages[key] ?? key;
      return formatMessage(template, params);
    },
    getAvailableLanguages() {
      return Object.keys(MESSAGES);
    },
  };
}
